import math

def solve():
    try:
        # Зчитуємо 6 чисел з одного рядка: x1, y1, r1, x2, y2, r2
        input_data = input()
        x1, y1, r1, x2, y2, r2 = map(float, input_data.split())
        
        # Обчислюємо відстань між центрами кіл
        d = math.sqrt((x2 - x1)**2 + (y2 - y1)**2)
        
        # Константа для порівняння чисел із плаваючою точкою
        eps = 1e-9
        
        # 1. Випадок безкінечної кількості точок (кола збігаються)
        if d < eps and abs(r1 - r2) < eps:
            print(-1)
            
        # 2. Випадок відсутності точок перетину
        # (занадто далеко або одне всередині іншого без торкання)
        elif d > r1 + r2 + eps or d < abs(r1 - r2) - eps:
            print(0)
            
        # 3. Випадок однієї точки перетину (торкання зовнішнє або внутрішнє)
        elif abs(d - (r1 + r2)) < eps or abs(d - abs(r1 - r2)) < eps:
            print(1)
            
        # 4. Випадок двох точок перетину
        else:
            print(2)
            
    except ValueError:
        print("Будь ласка, введіть 6 чисел через пробіл.")

if __name__ == "__main__":
    solve()