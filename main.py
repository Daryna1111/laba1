def main():
    # Зчитуємо три значення t1, t2, t3 з одного рядка
    try:
        input_data = input("Введіть три значення:\n")
        t1, t2, t3 = map(float, input_data.split())
        
        # Перевірка на випадок введення некоректних або нульових значень
        if t1 <= 0 or t2 <= 0 or t3 <= 0:
            print("Час повинен бути більшим за 0.")
            return

        # Обчислення спільного часу
        total_time = 1 / (1/t1 + 1/t2 + 1/t3)
        
        # Виведення результату з округленням до 2 знаків після коми
        print(f"Час, необхідний для з'їдання пирога: {total_time:.2f} годин")
        
    except ValueError:
        print("Будь ласка, введіть три числа через пробіл.")

if __name__ == "__main__":
    main()